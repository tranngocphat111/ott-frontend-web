const EMOJIS_SORTED_BY_LENGTH = Object.keys(EMOJI_TO_SHORTCODE_MAP).sort(
  (a, b) => b.length - a.length,
);

const EMOJI_SEGMENT_PATTERN = /\p{Extended_Pictographic}/u;

const splitByGrapheme = (value: string): string[] => {
  if (typeof Intl !== "undefined" && "Segmenter" in Intl) {
    const segmenter = new Intl.Segmenter(undefined, {
      granularity: "grapheme",
    });
    return Array.from(segmenter.segment(value), (segment) => segment.segment);
  }

  return Array.from(value);
};

const escapeHtml = (value: string): string => {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
};

const unescapeHtml = (value: string): string => {
  if (typeof document === "undefined") {
    return value;
  }

  const textarea = document.createElement("textarea");
  textarea.innerHTML = value;
  return textarea.value;
};

const decodeHtmlEntities = (value: string): string => {
  if (typeof document === "undefined") {
    return value;
  }

  const textarea = document.createElement("textarea");
  textarea.innerHTML = value;
  return textarea.value;
};

export const convertShortcodeToEmoji = (text: string): string => {
  if (!text || typeof text !== "string") {
    return String(text || "");
  }

  let normalized = decodeHtmlEntities(text);

  for (const shortcode of SHORTCODES_SORTED_BY_LENGTH) {
    if (!normalized.includes(shortcode)) {
      continue;
    }
    normalized = normalized
      .split(shortcode)
      .join(SHORTCODE_TO_EMOJI_MAP[shortcode]);
  }

  return normalized;
};

export const convertDisplayShortcodeToEmoji = (text: string): string => {
  if (!text || typeof text !== "string") {
    return String(text || "");
  }

  let normalized = decodeHtmlEntities(text);

  for (const shortcode of CUSTOM_SHORTCODES_SORTED_BY_LENGTH) {
    const escapedShortcode = escapeRegExp(shortcode);
    const pattern = new RegExp(`(^|\\s)(${escapedShortcode})(?=\\s|$)`, "g");

    normalized = normalized.replace(
      pattern,
      (_match, leadingWhitespace: string) => {
        return `${leadingWhitespace}${CUSTOM_SHORTCODE_TO_EMOJI_MAP[shortcode]}`;
      },
    );
  }

  // Keep existing long shortcode support like :grinning_face:
  return convertShortcodeToEmoji(normalized);
};

export const convertEmojiTextToImageMarkup = (
  text: string,
  emojiSize = 18,
): string => {
  if (!text || typeof text !== "string") {
    return String(text || "");
  }

  const normalizedText = convertShortcodeToEmoji(text);
  const graphemes = splitByGrapheme(normalizedText);

  return graphemes
    .map((segment) => {
      if (!EMOJI_SEGMENT_PATTERN.test(segment)) {
        return escapeHtml(segment);
      }

      const imageUrl = getEmojiImageUrl(segment);
      return `<img data-emoji="${escapeHtml(segment)}" alt="${escapeHtml(segment)}" src="${escapeHtml(imageUrl)}"  draggable="false" loading="lazy" style="display:inline-block;vertical-align:-0.12em;object-fit:contain" />`;
    })
    .join("");
};

export const convertEmojiImageMarkupToText = (text: string): string => {
  if (!text || typeof text !== "string") {
    return String(text || "");
  }

  const normalized = unescapeHtml(text);
  const container = document.createElement("div");
  container.innerHTML = normalized;

  const emojiImages = container.querySelectorAll("img[data-emoji]");
  emojiImages.forEach((image) => {
    const emoji = image.getAttribute("data-emoji") || "";
    image.replaceWith(document.createTextNode(emoji));
  });

  // Normalize browser artifacts from contentEditable.
  const plainText = (container.textContent || "")
    .replace(/\u00A0/g, " ")
    .replace(/[\u200B-\u200D\uFEFF]/g, "");

  return plainText;
};

export const convertEmojiToShortcode = (text: string): string => {
  if (!text || typeof text !== "string") {
    return String(text || "");
  }

  let normalized = decodeHtmlEntities(text);

  for (const emoji of EMOJIS_SORTED_BY_LENGTH) {
    if (!normalized.includes(emoji)) {
      continue;
    }
    normalized = normalized.split(emoji).join(EMOJI_TO_SHORTCODE_MAP[emoji]);
  }

  return normalized;
};

export const getShortcodeFromEmoji = (emoji: string): string => {
  return EMOJI_TO_SHORTCODE_MAP[emoji] || emoji;
};
