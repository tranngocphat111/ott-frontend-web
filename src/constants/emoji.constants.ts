const normalizeEmojiForImage = (emoji: string): string[] => {
  return Array.from(emoji)
    .map((char) => char.codePointAt(0)?.toString(16))
    .filter((codepoint): codepoint is string =>
      Boolean(codepoint && codepoint !== "fe0f"),
    );
};

export const getEmojiImageUrl = (emoji: string): string => {
  const normalizedCodepoints = normalizeEmojiForImage(emoji).join("-");
  return `${EMOJI_IMAGE_BASE_URL}/${normalizedCodepoints}.png`;
};

